'''
Lesson 05 - Boolean
boolean
logical operators and or not
comparison operators == != > < >= <=
'''
t = True
f = False
print(t, f)

print(t and f, t and t)
print(t or f or f, f or f)
print(not t, not f)

b = 3 == 3
print(type(b), b)
print(3 != 3)
print(3 > 3)
print(3 < 3)
print(3 >= 3)
print(3 <= 3)